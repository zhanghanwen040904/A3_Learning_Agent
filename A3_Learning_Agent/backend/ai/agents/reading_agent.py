from .resource_base import StructuredResourceAgent


class ReadingAgent(StructuredResourceAgent):
    resource_type = "reading"
    role = "前沿拓展阅读策展师"
    goal = "围绕课程知识和学生目标组织有导读、有问题链的拓展阅读"
    default_title = "个性化拓展阅读"
    requirements = """
必须生成一篇可直接阅读的完整拓展阅读正文，不能只返回标题、图片或摘要字段。
内容必须包含以下 Markdown 小节：
# 拓展阅读主题
## 为什么值得读
## 与课程知识点的连接
## 拓展知识讲解
## 前沿应用或真实场景
## 分段导读
## 阅读思考问题
## 进一步探索方向
可以基于大模型已有知识和课程知识库内容扩展相关概念、应用场景、行业实践和学习路线，但不得虚构具体论文、作者、链接或不存在的教材页码；无法核验的内容应明确标为“探索建议”。
正文不少于 800 字，必须围绕学生薄弱点和当前阶段知识点展开，避免空泛介绍。
""".strip()

    def _fallback_resource(self, context, knowledge_text):
        points = self._context_points(context)
        topic = "、".join(points) or context.get("study_goal") or "软件工程拓展学习"
        excerpt = str(knowledge_text or "").strip()[:520] or "请结合课程教材中的相关章节理解概念、流程、案例和常见误区。"
        content = f"""# {topic}拓展阅读

## 为什么值得读
围绕“{topic}”进行拓展阅读，可以帮助你把教材中的定义、流程和阶段产物与真实软件项目联系起来。对于软件工程学习而言，很多知识点并不是孤立概念，而是会在需求、设计、实现、测试和维护之间反复流动。

## 与课程知识点的连接
本阅读材料连接的核心知识点包括：{topic}。它们通常对应课程中的概念理解、过程建模、文档产出和质量保证问题。

## 拓展知识讲解
{excerpt}

在实际项目中，学习这些内容时建议关注三个问题：第一，这个知识点解决什么软件工程问题；第二，它输入什么信息、输出什么产物；第三，如果忽略它，会给后续设计、测试或维护带来什么风险。

## 前沿应用或真实场景
探索建议：在敏捷开发、DevOps、自动化测试和需求管理平台中，这些知识点通常会被工具化。例如，需求分析会被拆解成用户故事、验收标准和优先级；测试活动会通过持续集成流水线自动执行；维护阶段会结合缺陷跟踪、日志分析和版本发布策略进行管理。

## 分段导读
1. 先阅读教材中的定义和流程，明确核心术语。
2. 再结合案例理解每个阶段的输入、活动和输出。
3. 最后思考该知识点如何影响测试、维护和软件质量。

## 阅读思考问题
- 这个知识点在软件生命周期中处于什么位置？
- 它与前后阶段的关系是什么？
- 如果这一环节做得不好，会造成哪些后果？
- 如何用一个课程案例说明它的作用？

## 进一步探索方向
探索建议：可以继续了解需求管理工具、测试管理平台、自动化测试、软件质量度量和缺陷生命周期管理等方向，并尝试把它们与课程中的基础概念对应起来。
"""
        return {
            "resource_type": self.resource_type,
            "title": f"{topic}拓展阅读",
            "content": content,
            "knowledge_points": points,
            "personalization": f"围绕学生薄弱点“{topic}”生成拓展阅读正文，补充教材之外的应用场景和思考问题。",
            "format": "markdown",
            "agent_name": self.__class__.__name__,
        }
